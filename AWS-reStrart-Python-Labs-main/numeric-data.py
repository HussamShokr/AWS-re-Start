print("Python has three numeric types: int, float, and complex")


# Creating and Using an Integer Variable
myValue = 1  # Assigns the integer value 1 to the variable myValue
print(myValue)  # Prints the value of myValue, which is 1
print(type(myValue))  # Prints the type of myValue, which is <class 'int'>
print(
    str(myValue) + " is of the data type " + str(type(myValue))
)  # Converts myValue and its type to strings and prints them

# Creating and Using a Float Variable
myValue = 3.14  # Assigns the float value 3.14 to the variable myValue
print(myValue)  # Prints the value of myValue, which is 3.14
print(type(myValue))  # Prints the type of myValue, which is <class 'float'>
print(
    str(myValue) + " is of the data type " + str(type(myValue))
)  # Converts myValue and its type to strings and prints them


