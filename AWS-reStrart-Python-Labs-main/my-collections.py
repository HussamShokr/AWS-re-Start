# 1 Create a list
myFruitList = ["apple", "banana", "cherry"]

# 2 Print the list and its type
print(myFruitList)
print(type(myFruitList))

# 3 Access items in the list by position
print(myFruitList[0])  # Prints "apple"
print(myFruitList[1])  # Prints "banana"
print(myFruitList[2])  # Prints "cherry"

# 4 Changing the values in a list
# 4.1 Modify an item in the list
myFruitList[2] = "orange"
print(myFruitList)

# 5 Introducing the tuple data type
# 5.1 Creating a tuple Print the tuple and its type
myFinalAnswerTuple = ("apple", "banana", "pineapple")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

# 5.2 Accessing a tuple by position
# Access each item in the tuple by its index
print(myFinalAnswerTuple[0])  # Prints "apple"
print(myFinalAnswerTuple[1])  # Prints "banana"
print(myFinalAnswerTuple[2])  # Prints "pineapple"

# Introducing the dictionary data type
# 1 Defining a dictionary Create a dictionary
myFavoriteFruitDictionary = {"Akua": "apple", "Saanvi": "banana", "Paulo": "pineapple"}

# 2 Print the dictionary and its type
print(myFavoriteFruitDictionary)
